#[test]
fn typescript_contracts_are_current() {
    let expected = generated_typescript_contracts();
    let path = generated_contracts_path();

    if env::var_os("NOLOONG_UPDATE_TS_CONTRACTS").is_some() {
        fs::create_dir_all(path.parent().expect("generated contracts directory"))
            .expect("create generated contracts directory");
        fs::write(&path, &expected).expect("write generated TypeScript contracts");
    }

    let actual = fs::read_to_string(&path).unwrap_or_else(|error| {
        panic!(
            "{} is missing or unreadable: {error}. Run NOLOONG_UPDATE_TS_CONTRACTS=1 cargo test -p noloong-app typescript_contracts_are_current",
            path.display()
        );
    });

    assert_eq!(
        actual,
        expected,
        "{} is stale. Run NOLOONG_UPDATE_TS_CONTRACTS=1 cargo test -p noloong-app typescript_contracts_are_current",
        path.display()
    );
}

fn generated_typescript_contracts() -> String {
    let contracts = contracts();
    let mut output = String::from(
        "/* eslint-disable */\n\
         // This file is generated by crates/noloong-app/tests/typescript_contracts.rs.\n\
         // Do not edit it by hand.\n\n",
    );

    for contract in &contracts {
        output.push_str(&contract.render_schema_const());
    }

    output.push_str(&render_type_declarations(&contracts));
    output
}

fn render_type_declarations(contracts: &[TypescriptContract]) -> String {
    let mut definitions = BTreeMap::new();
    for contract in contracts {
        collect_definitions(&contract.schema_value(), &mut definitions);
    }

    let root_names = contracts
        .iter()
        .map(|contract| contract.type_name)
        .collect::<BTreeSet<_>>();
    let mut output = String::from("export type JsonUnknown = unknown;\n\n");

    for (name, schema) in definitions {
        let type_name = sanitize_type_name(&name);
        if root_names.contains(type_name.as_str()) {
            continue;
        }
        output.push_str(&render_type_alias(&type_name, &schema));
    }

    for contract in contracts {
        output.push_str(&render_type_alias(
            contract.type_name,
            &contract.schema_value(),
        ));
    }

    output
}

